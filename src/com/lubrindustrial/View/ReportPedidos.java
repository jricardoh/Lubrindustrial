    private void agregarDatos() {
        // ********** CAMBIO PARA MOSTRAR LAS 2 NUEVAS COLUMNAS DE LA BD

        DefaultTableModel model = (DefaultTableModel) tab_pedidos.getModel();

        model.setRowCount(0);
        String datos[] = new String[11];//ARRAY DE 3

        //LE PASO AL ARRAY LOS DATOS DEL ARRAYLIST 
        PEDIDOMATERIALCRUD pedCRUD = new PEDIDOMATERIALCRUD();

        pedidos = pedCRUD.visualizar(); // devuelve todos los registros de la BD

        for (PEDIDOMATERIAL p : pedidos) {

            datos[0] = Integer.toString(p.getIdPedido());
            datos[1] = p.getDescArt();
            datos[2] = Float.toString(p.getCantidad());
            datos[3] = p.getUnidadCantidad();
            datos[4] = p.getDescripcion();
            datos[5] = p.getDescEmpPedido();
            datos[6] = p.getDescEmpEntregado();
            datos[7] = p.getDescEmpAutorizado();
            datos[8] = p.getDescEmpAprobado();
            datos[9] = p.getFechaHoraSolicitud();
            datos[10] = p.getFechaHoraEntrega();
            
            model.addRow(datos);
        }

    }


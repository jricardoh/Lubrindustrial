    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        PEDIDOMATERIALCRUD pedCRUD = new PEDIDOMATERIALCRUD(host);
        PEDIDOMATERIAL ped = new PEDIDOMATERIAL();
        String cad="";
        int idAux=0;
        float stock=0;
        String cadStock="";
        
        try{
            cad=cmbox_materiales.getSelectedItem().toString();
            String []split = cad.split("-");
            cad = split[0];
            idAux=Integer.parseInt(cad);
            ped.setIdArt(idAux);
            
            cad=cmbox_EmpPedido.getSelectedItem().toString();
            String []split2 = cad.split(" ");
            cad = split2[0];
            idAux=Integer.parseInt(cad);
            ped.setIdEmpPedido(idAux);
            
            cad=cmbox_EmpEntregado.getSelectedItem().toString();
            String []split3 = cad.split(" ");
            cad = split3[0];
            idAux=Integer.parseInt(cad);
            ped.setIdEmpEntregado(idAux);
            
            cad=cmbox_EmpAutorizado.getSelectedItem().toString();
            String []split4 = cad.split(" ");
            cad = split4[0];
            idAux=Integer.parseInt(cad);
            ped.setIdEmpAutorizado(idAux);
            
            cad=cmbox_EmpAprobado.getSelectedItem().toString();
            String []split5 = cad.split(" ");
            cad = split5[0];
            idAux=Integer.parseInt(cad);
            ped.setIdEmpAprobado(idAux);
            
            ped.setCantidad(Float.parseFloat(txt_cantidad.getText()));
            ped.setUnidadCantidad(txt_unidad.getText());
            ped.setDescripcion(txt_desc.getText());
            ped.setFechaHoraEntrega(txt_fechaEntrega.getText());
            ped.setFechaHoraSolicitud(txt_fechaSolicitud.getText());
            
            // a.getIdArt()+"- "+a.getNroArt()+" "+a.getDescArt()+": "+a.getCantidad()+" "+a.getDescripCantidad()
            cadStock = cmbox_materiales.getSelectedItem().toString();
            String []words = cadStock.split(":");
            String []words1 = words[1].split(" ");
            stock = Float.parseFloat(words1[1]);
            //JOptionPane.showMessageDialog(null, "STOCK: "+stock,"STOCK ACTUAL",JOptionPane.INFORMATION_MESSAGE);
            if(ped.getCantidad()>stock){
                JOptionPane.showMessageDialog(null, "NO EXISTE LA CANTIDAD SOLICITADO EN STOCK DEL MATERIAL. REVISE QUE SEA CORRECTA E INFERIOR A LA DISPONIBLE","ERROR STOCK", JOptionPane.ERROR_MESSAGE);
            }else{
                if(pedCRUD.insertar(ped)){
                    JOptionPane.showMessageDialog(null, "PEDIDO REGISTRADO","REGISTRO EXITOSO",JOptionPane.INFORMATION_MESSAGE);
                    llenarComboBoxEmpleados();
                    llenarComboBoxArticulos();
                //Volver();
                }else{
                    JOptionPane.showMessageDialog(null, "PEDIDO NO REGISTRADO","ERROR EN EL REGISTRO", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error obtener los datos: "+e.getMessage(), "ERROR DATOS", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void llenarComboBoxEmpleados(){
        EmployeeCRUD empsCRUD = new EmployeeCRUD(host);
//        int idDep=0;
//        
//        String dep = cmbox_departamentos.getSelectedItem().toString();
//        String []cad = dep.split(" ");
//        idDep = Integer.parseInt(cad[0]);
        
        empleados = empsCRUD.visualizarPorDepartamento(1); // devuelve todos los registros de la BD
        
        cmbox_EmpPedido.removeAllItems();
        cmbox_EmpEntregado.removeAllItems();
        cmbox_EmpAprobado.removeAllItems();
        cmbox_EmpAutorizado.removeAllItems();
        
        for(Employee e: empleados){  
            cmbox_EmpPedido.addItem(e.getIdEmployee()+" "+e.getNomEmployee()+" "+e.getApeEmployee()+" "+e.getNameDept());
            cmbox_EmpEntregado.addItem(e.getIdEmployee()+" "+e.getNomEmployee()+" "+e.getApeEmployee()+" "+e.getNameDept());
            cmbox_EmpAprobado.addItem(e.getIdEmployee()+" "+e.getNomEmployee()+" "+e.getApeEmployee()+" "+e.getNameDept());
            cmbox_EmpAutorizado.addItem(e.getIdEmployee()+" "+e.getNomEmployee()+" "+e.getApeEmployee()+" "+e.getNameDept());
        }
    }
    
    private void llenarComboBoxArticulos(){
        ArticleCRUD artsCRUD = new ArticleCRUD(host);
        
        articulos = artsCRUD.visualizarSTOCK(); // devuelve todos los registros de la BD
        
        cmbox_materiales.removeAllItems();
        
        for(Article a: articulos){  
            cmbox_materiales.addItem(a.getIdArt()+"- "+a.getNroArt()+" "+a.getDescArt()+": "+a.getCantidad()+" "+a.getDescripCantidad());
        }
    }

